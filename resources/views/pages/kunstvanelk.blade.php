@section('case-explanation-titel')
    Wat was de case?
@endsection

@section('case-explanation-een-beschrijving')
    De uitdaging van deze case lag in het ontwikkelen van een prachtige website die niet alleen aantrekkelijk was voor de bezoekers, maar ook gemakkelijk te beheren was voor de klant zelf. Het was van essentieel belang dat de schilderijen op een uitnodigende en aansprekende manier werden gepresenteerd, waardoor de drempel voor potentiële kopers zo laag mogelijk werd gehouden. De klant wilde immers niet alleen een online galerij creëren, maar ook een platform waarop hij zich kon richten op zijn passie voor schilderen, zonder zich zorgen te hoeven maken over ingewikkeld websitebeheer.
@endsection

@section('case-explanation-twee-beschrijving')
    Om aan deze behoeften te voldoen, was het cruciaal om een systeem te gebruiken waarbij de klant met minimale inspanning nieuwe schilderijen kon toevoegen en bestaande werken kon bijwerken. Dit zou niet alleen het onderhoud van de website vereenvoudigen, maar ook de klant in staat stellen om zich te concentreren op wat echt belangrijk voor hem was: het creëren van prachtige kunstwerken.
@endsection

@section('case-details-een-titel')
    Uitdagingen
@endsection

@section('case-details-een-een-beschrijving')
    De uitdaging van deze website lag echt bij het ontwerp. Het simpel, maar toch visueel aantrekkelijk krijgen van de website. Al zeg ik het zelf denk ik echt dat dit is gelukt. Het is een mooie en overzichtelijke website waar bezoekers gelijk weten wat ze te wachten staat.
@endsection

@section('case-details-een-twee-beschrijving')
    Het beheren van de website moest ook makkelijk en zonder gedoe verlopen. Daarom heb ik gebruik gemaakt van WordPress, omdat je hierin makkelijk dingen kon aanpassen en toevoegen. Met behulp van ACF heb ik velden gemaakt die ingevuld kunnen worden voor de prijs van het schilderij, de titel en de beschrijving. Zo kon er een vaste lay-out worden gemaakt die dynamisch werd gevuld met de content van dat specifieke schilderij.
@endsection

