@section('cases-beschrijving')
    Bij deze case stonden we voor een uitdaging vol nieuwe ervaringen. Voor het eerst gingen we aan de slag met Python en moesten we samenwerken met een compleet nieuwe groep mensen. Onze opdracht: kies uit drie verschillende games om te maken - "Side scrolling shoot 'm up", "Lunar Lander" of "Single Screen Platformer". Wij besloten voor een variant van "Side scrolling shoot 'm up" te gaan, maar met een twist: we wilden ook een platformgame creëren die ons deed denken aan de klassieke Mario-spellen. Het was een avontuur vol ontdekkingen, uitdagingen en creatieve oplossingen. We hebben nieuwe vaardigheden geleerd en onszelf uitgedaagd om iets unieks te creëren.
