@section('case-punt-twee-beschrijving')
    Bij deze game konden we onze creativiteit volledig de vrije loop laten. Zo implementeerden we een achtergrond die meebewoog met de speler, wat een dynamisch effect gaf aan het spel. Daarnaast kregen we de kans om onze eigen vijanden te bedenken en hun gedrag te programmeren, evenals hoe de speler ze kon verslaan. Het was een fantastische ervaring waarbij we onze verbeelding konden gebruiken en onze ideeën tot leven konden brengen in de game.
