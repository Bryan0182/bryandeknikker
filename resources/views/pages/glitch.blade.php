@section('case-explanation-een-beschrijving')
    De "GLITCH" (Gamified Learning Interface Through Challenges and Heuristics) is een innovatief en modulair leerplatform ontworpen om studenten op een dynamische en interactieve manier onderwijs te bieden. Het platform is zowel als mobiele app en webapplicatie beschikbaar en maakt gebruik van gamification om de leerervaring te verbeteren. Studenten kunnen cursussen en modules binnen specifieke domeinen volgen, waarbij elke module bestaat uit activiteiten met toenemende moeilijkheidsgraad. Het platform biedt ook uitdagingen zoals Point en Concept Challenges om studenten te motiveren en hen te helpen hun kennis in praktijk te brengen. Docenten kunnen via een admin dashboard de voortgang van studenten volgen, activiteiten goedkeuren en feedback geven.
@endsection

@section('case-explanation-twee-beschrijving')
    Het ontwikkelteam moet een cross-platform applicatie bouwen met React Native voor zowel iOS als Android, ondersteund door een MVC-architectuur en een (SQL) database. Belangrijke functies zijn onder andere gebruikersauthenticatie, een dynamisch gebruikersdashboard, voortgangsvalidatie door docenten, en een systeem voor push-notificaties. Het platform moet flexibel zijn, zodat thema's eenvoudig kunnen worden aangepast, en het project moet vergezeld gaan van uitgebreide technische documentatie en een goed gestructureerde repository. Het doel is om een gebruiksvriendelijke en uitdagende leeromgeving te creëren die studenten niet alleen motiveert maar ook hun academische groei ondersteunt.
@endsection

